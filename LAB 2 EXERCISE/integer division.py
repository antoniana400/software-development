half = int(1/2) # Casted to integer yo make a point
print(f"One half (1/2) = {half}")
print(type(half))

# Using decimals i.e.(float values) (1.0 and 2.0) tells Python and a code reader that we are working with floating-point numbers.
half = 1.0 / 2.0

print(f"One half (1/2) = {half}")

# This will now output <class 'float'> instead of <class 'int'>
print(type(half))